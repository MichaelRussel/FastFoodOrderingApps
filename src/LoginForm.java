    public int checkUsernameList(String username){
        int customerIndex = 0;
        for(int i = 0 ; i <= arraySize ;i++){
            if(username.equals(userName[i])){
                this.customerIndex = customerIndex;
                return customerIndex;
            }
        }
        return -1;
    }
    
