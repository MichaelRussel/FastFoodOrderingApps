    public boolean checkNumeric(){
        for(int i = 0 ; i<phoneTxt.getText().length();i++){
            if(!Character.isDigit(phoneTxt.getText().charAt(i))){
                return false;
            }
        }
        return true;
    }
    public boolean checkAlphaNum(){
        boolean a = false;
        boolean b = false;

        for(int i = 0; i<passwordTxt.getText().length(); i++){ 
            if(Character.isAlphabetic(passwordTxt.getText().charAt(i))){
                a = true;
            }
            if(Character.isDigit(passwordTxt.getText().charAt(i))){
                b = true;
            }
            if((a && b) == true){
                return true;
            }            
        }
        return false;
    }
    
    public boolean checkUsername(){
        if(usernameTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Username must be filled!", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(usernameTxt.getText().length() <= 3){
            JOptionPane.showMessageDialog(null, "Username must be more than 3 characters!", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else {
            customerData[i] = new RegisterWindow();
            customerData[i].username = usernameTxt.getText();
            return true;
        }
    }
    public boolean checkEmail(){
        if(emailTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Email must be filled!", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (!(Pattern.matches("^[a-zA-Z0-9]+[@]{1}+[a-zA-Z0-9]+[.]{1}+[a-zA-Z0-9]+$", emailTxt.getText())))
        {
            JOptionPane.showMessageDialog(null, "Email must be valid!", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else{ 
            customerData[i].email = emailTxt.getText();
            return true;
        
        }
        
    } 
    public boolean checkPassword(){
        if(passwordTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Password must be filled!", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(!checkAlphaNum()){
            JOptionPane.showMessageDialog(null, "Password must be alphanumeric!", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else return true;
    }
    public boolean checkConfPassword(){
        if(confPasswordTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Confirm Password must be filled!", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(!confPasswordTxt.getText().equals(passwordTxt.getText())){
            JOptionPane.showMessageDialog(null, "Confirm Password must be equals to Password!", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else{
            customerData[i].password = confPasswordTxt.getText();
            return true;
        } 
    }
    public boolean checkGender(){
        if(!femaleBtn.isSelected() && !maleBtn.isSelected()){
            JOptionPane.showMessageDialog(null, "Gender must be selected!", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else {
            if(femaleBtn.isSelected())
                customerData[i].gender = femaleBtn.getText();
            else
                customerData[i].gender = maleBtn.getText();
            return true;
        }
    }
    public boolean checkPhone(){
        if(phoneTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Phone Number must be filled!", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(!checkNumeric()){
            JOptionPane.showMessageDialog(null, "Phone Number must be Numeric!", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else{ 
            customerData[i].phone = phoneTxt.getText();
            return true;
        }
        
    }
    public boolean checkAddress(){
        if(addressTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Address must be filled!", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(addressTxt.getText().length() <= 8){
            JOptionPane.showMessageDialog(null, "Address must be more than 8 characters!", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else {
            customerData[i].address = addressTxt.getText();
            return true;
        }
    }
    
