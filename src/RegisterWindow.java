    private void ResetBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ResetBtnActionPerformed
         int n = JOptionPane.showConfirmDialog(null, "Are you sure you want to reset?", "Reset Option", JOptionPane.YES_NO_OPTION);
        if(n == JOptionPane.YES_OPTION){
            usernameTxt.setText("");
            emailTxt.setText("");
            passwordTxt.setText("");
            confPasswordTxt.setText("");
            genderGroup.clearSelection();
            phoneTxt.setText("");
            addressTxt.setText("");
        }else{
            System.out.println("Reset cancelled.");
        }
        
    }//GEN-LAST:event_ResetBtnActionPerformed
    
    //Add save registered data and redirect to login form
    private void RegisterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterBtnActionPerformed
        if(
                checkUsername()&&
                checkEmail() &&
                checkPassword() &&
                checkConfPassword() &&
                checkGender() &&
                checkPhone()&&
                checkAddress()
                ){

            i++;
            
            JOptionPane.showMessageDialog(null, "Success register account");
            
            new LoginForm(customerData).setVisible(true);
            this.dispose();
            
        }
    }//GEN-LAST:event_RegisterBtnActionPerformed

